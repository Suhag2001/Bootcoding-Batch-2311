package org.example.data_type;

class ForLoop {

    public static void main(String args[]) {

//for(declaration/intiaization ; condition ; incre/decre){

//code  }

        int i = 1;

        for (i = 1; i <= 2; i++) {
            System.out.println(i + " java");

            for (int j = 1; j <= 5; j++) {
                System.out.println(j + " Python");
            }

        }

    }
}